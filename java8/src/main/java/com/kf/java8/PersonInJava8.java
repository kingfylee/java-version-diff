package com.kf.java8;

public class PersonInJava8 {

    private String name;

    private int age;

    public PersonInJava8() {
    }

    public PersonInJava8(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
